// Metodo includes = Determina se um array contém um determinado elemento, retornando 'true' ou 'false'. 

const numeros = [10, 20, 30, 40, 50];

console.log(numeros.includes(80));

// Método indexof = Retorna o primeiro índice em que o elemento pode ser encontrado no array. Exemplo:

const animais = ['Cachorro', 'Gato', 'Papagaio', 'Tartaruga', 'Peixe'];

console.log(animais.indexOf('Tartaruga'));
