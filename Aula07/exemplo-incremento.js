// i++ -> Pós-incremento: Ele primeiro retorna o valor atual de i e depois incrementa i em 1 unidade. 
// Exemplo: 

let i = 5; 
console.log(i++);
console.log(i);
console.clear();

// ++i -> Pré-incremento: Ele primeiro incrementa i em 1 unidade e depois retorna o novo valor de i. 
//Exemplo: 

let j = 8;
console.log(++j);
console.log(j);
console.clear();

let x = 5;
let valorA = x++;
let valorB = ++x;

console.log("O valor de valorA é:", valorA);
console.log("O valor de valorB é:", valorB);